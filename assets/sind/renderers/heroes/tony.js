function initAnimations(renderer){
    addAnimation(renderer, "summonpose", "sind:summon_pose").setData((entity, data) => {
        data.load(0, Math.max(entity.getInterpolatedData("sind:dyn/summon7_timer"), entity.getInterpolatedData("sind:dyn/summon_timer")));
    }).priority = -11;
    addAnimation(renderer, "summon", "sind:summon").setData((entity, data) => {
        var nbt = entity.getWornChestplate().nbt();
        var pick = nbt.getString("Pick").split("sind:")[1];
        data.load(0, (pick == "mark38" || pick == "mark38/rg") ? 0 : entity.getInterpolatedData("sind:dyn/summon_timer"));
    }).priority = 14;
    addAnimation(renderer, "mk7", "sind:mk7_transformation").setData((entity, data) => {
        data.load(0, entity.getInterpolatedData("sind:dyn/summon7_timer"));
    }).priority = 14;
    addAnimation(renderer, "glasses", "sind:glasses").setData((entity, data) => {
        data.load(entity.getInterpolatedData("sind:dyn/edith_timer"));
    }).priority = 1;
}
